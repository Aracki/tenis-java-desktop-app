    private void btnObrisiTakmicaraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiTakmicaraActionPerformed
        if (tblTakmicari.getSelectedRowCount() == 1) {
            takmicarZaDelete = Podaci.listaTakmicara.get(tblTakmicari.getSelectedRow());
            
            KlijentTransferObjekat kto = new KlijentTransferObjekat();
            kto.setObjekat(takmicarZaDelete);
            kto.setOperacija(Konstante.OPERACIJA_DELETE_TAKMICARA);
            
            try {
                Kontroler.getInstance().posaljiObjekat(kto);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            refreshTabelu();
            napuniTblTakmicare();
        }
    }//GEN-LAST:event_btnObrisiTakmicaraActionPerformed

