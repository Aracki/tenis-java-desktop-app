    private void cbxTakmicenjaTAKItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxTakmicenjaTAKItemStateChanged
        if (evt.getStateChange() == 1) {
            Takmicenje t = (Takmicenje) cbxTakmicenjaTAK.getSelectedItem();
            List<Liga> ls = new ArrayList<>();
            for (Liga l : Podaci.listaLiga) {
                if (l.getTakmicenje().getTakmicenjeID() == t.getTakmicenjeID()) {
                    ls.add(l);
                }
            }
            cbxLigaTAK.setModel(new DefaultComboBoxModel(ls.toArray()));
            if (cbxLigaTAK.getItemCount() > 0) {
                cbxLigaTAK.setSelectedIndex(0);
                liga_trenutna_tabTakmicari = (Liga) cbxLigaTAK.getSelectedItem();
            }
        }
    }//GEN-LAST:event_cbxTakmicenjaTAKItemStateChanged

