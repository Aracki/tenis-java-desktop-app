    private void napuniTabeluTakmicaraLige() {
        
        if (liga_trenutna_tabMecevi != null) {
            List<Takmicar> pomLista = new ArrayList<>();
            for (Takmicar tak : Podaci.listaTakmicara) {
                if (tak.getLiga().getLigaID() == liga_trenutna_tabMecevi.getLigaID()) {
                    pomLista.add(tak);
                }
            }
            if (!pomLista.isEmpty()) {
                pomLista.sort(new CustomComparator());
                DflPDF.setPomLista(pomLista);
                tblTakmicariTeLige.setModel(new TableModelTakmicariZaLigu(pomLista));
            }
        }
    }
    
    private void srediPretragu() {
        jtfPretraga.getDocument().addDocumentListener(new DocumentListener() {
            
            @Override
            public void insertUpdate(DocumentEvent e) {
                ((TableModelTakmicari) tblTakmicari.getModel()).skloniSaListe(jtfPretraga.getText());
            }
            
            @Override
            public void removeUpdate(DocumentEvent e) {
                ((TableModelTakmicari) tblTakmicari.getModel()).vratiNaListu(jtfPretraga.getText());
            }
            
            @Override
            public void changedUpdate(DocumentEvent e) {
                JOptionPane.showMessageDialog(null, "nesto se desilo!");
            }
            
        });
    }
    
