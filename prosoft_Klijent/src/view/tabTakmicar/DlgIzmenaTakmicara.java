    private void btnUnesiTakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiTakActionPerformed
        System.out.println("GGG" + Podaci.listaTakmicara);

        for (Takmicar t11 : Podaci.listaTakmicara) {
            System.out.println("&&&&" + t11.getTakmicarID());
        }

        try {
            System.out.println("#" + frmGlavna.getT1().getTakmicarID());
            Takmicar t1 = frmGlavna.getT1();
            t1.setTakmicarID(frmGlavna.getT1().getTakmicarID());
            t1.setOpis(txtOpis.getText().trim());
            t1.setFb_link(txtFBLINK.getText().trim());
            t1.setLiga((Liga) cbxLige.getSelectedItem());
            t1.setMesto((Mesto) cbxMesta.getSelectedItem());
            System.out.println("#####" + t1.getTakmicarID());

            KlijentTransferObjekat kto = new KlijentTransferObjekat();
            kto.setOperacija(Konstante.OPERACIJA_UPDATE_TAKMICAR);
            kto.setObjekat(t1);

            Kontroler.getInstance().posaljiObjekat(kto);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska sa unosom podataka!");
        }


    }//GEN-LAST:event_btnUnesiTakActionPerformed

