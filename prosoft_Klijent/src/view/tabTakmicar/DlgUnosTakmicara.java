    private void btnUnesiTakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiTakActionPerformed
        Takmicar tak = new Takmicar();
        tak.setIme(txtIme.getText().trim());
        tak.setPrezime(txtPrezime.getText().trim());
        tak.setOpis(txtOpis.getText().trim());
        tak.setMesto((Mesto) cbxMestaTakmicara.getSelectedItem());
        tak.setLiga(frmGlavna.getLiga_trenutna_tabTakmicari());

        KlijentTransferObjekat kto = new KlijentTransferObjekat();
        kto.setOperacija(Konstante.OPERACIJA_KREIRAJ_TAKMICARA);
        kto.setObjekat(tak);
        try {
            Kontroler.getInstance().posaljiObjekat(kto);
            Kontroler.getInstance().posaljiZahtevZaPodatke();
            frmGlavna.napuniTblTakmicare();
            this.dispose();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnUnesiTakActionPerformed

