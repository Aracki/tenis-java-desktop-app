    private void btnUnesiTakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiTakActionPerformed
        Takmicenje t1 = t;
        t1.setNaziv(txtNaziv.getText());
        t1.setTiptakmicenja((TipTakmicenja) cbxTipTakmicanja.getSelectedItem());
        KlijentTransferObjekat kto = new KlijentTransferObjekat();
        kto.setOperacija(Konstante.OPERACIJA_UPDATE_TAKMICENJE);
        kto.setObjekat(t1);
        try {
            Kontroler.getInstance().posaljiObjekat(kto);
            frmGlavna.refreshTabelu();
            JOptionPane.showMessageDialog(this, "Uspesan update");
            this.dispose();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Nema konekcije sa serverom");
        }
        
    }//GEN-LAST:event_btnUnesiTakActionPerformed

