    public void napuniView(Takmicenje t) {
        this.t = t;
        txtNaziv.setText(t.getNaziv());
        lblDatum.setText(t.getDatum_pocetka().toString());
        listaTT = Podaci.listaTipovaTakmicenja;
        DefaultComboBoxModel dt = new DefaultComboBoxModel(listaTT.toArray());
        dt.setSelectedItem(t.getTiptakmicenja());
        cbxTipTakmicanja.setModel(dt);
    }

